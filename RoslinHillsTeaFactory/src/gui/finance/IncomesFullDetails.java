    private void loadIncomes() {

        String query = "SELECT * FROM incomes";

        boolean hasCondition = false;

        if (jTextField1.getText() != null && !jTextField1.getText().trim().isEmpty()) {
            query += " WHERE incomes.reason LIKE '%" + jTextField1.getText().trim() + "%'";
            hasCondition = true;
        }

        if (jComboBox1.getSelectedItem() != null) {
            
            String orderBy = "";

            if (jComboBox1.getSelectedItem().equals("Date Latest first")) {
                orderBy = " ORDER BY incomes.date DESC";
            } else if (jComboBox1.getSelectedItem().equals("Date Earlier first")) {
                orderBy = " ORDER BY incomes.date ASC";
            } else if (jComboBox1.getSelectedItem().equals("Amount low to high")) {
                orderBy = " ORDER BY incomes.total_amount ASC";
            } else if (jComboBox1.getSelectedItem().equals("Amount high to low")) {
                orderBy = " ORDER BY incomes.total_amount DESC";
            }

            query += orderBy;
        }

        try {
            
            ResultSet rs = MySQL.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getDate("date"));
                vector.add(rs.getString("reason"));
                vector.add(rs.getDouble("total_amount"));
                model.addRow(vector);
            }
        } catch (Exception ex) {
            Logger.getLogger(IncomesFullDetails.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void tableCenter() {

        // Custom renderer to center-align cells
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

        // Apply the renderer to all columns in jTable1
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }

    }

