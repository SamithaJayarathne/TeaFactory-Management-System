    private void loadRecords() {

        try {

            String query = "SELECT * FROM `overtime` INNER JOIN `employees` ON "
                    + "`employees`.`nic`=`overtime`.`employees_nic` INNER JOIN `approvel_status` ON "
                    + "`overtime`.`approvel_status_id`=`approvel_status`.`id`";

            if (jTextField1.getText() != null && !jTextField1.getText().trim().isEmpty()) {
                query += " WHERE `employees`.`nic` LIKE '"+jTextField1.getText()+"%'";
            }

// Handle sorting based on the combo box selection
            int index = jComboBox1.getSelectedIndex();
            String orderByClause = ""; // To store the ORDER BY clause
            switch (index) {
                case 1:
                    orderByClause = " ORDER BY `overtime`.`working_hours` ASC";
                    break;
                case 2:
                    orderByClause = " ORDER BY `overtime`.`working_hours` DESC";
                    break;
                case 3:
                    orderByClause = " ORDER BY `overtime`.`date` DESC";
                    break;
                case 4:
                    orderByClause = " ORDER BY `overtime`.`date` ASC";
                    break;
            }

// Append the ORDER BY clause if necessary
            query += orderByClause;

            ResultSet overtimeRecord = MySQL.executeSearch(query);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (overtimeRecord.next()) {

                Vector vector = new Vector();
                vector.add(overtimeRecord.getString("overtime.employees_nic"));
                vector.add(overtimeRecord.getString("employees.fname") + " " + overtimeRecord.getString("employees.lname"));
                vector.add(overtimeRecord.getString("overtime.working_hours"));
                vector.add(overtimeRecord.getString("overtime.date"));
                vector.add(overtimeRecord.getString("overtime.start"));
                vector.add(overtimeRecord.getString("overtime.end"));
                vector.add(overtimeRecord.getString("approvel_status.name"));

                model.addRow(vector);

            }

        } catch (Exception ex) {
            Logger.getLogger(OvertimeView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

