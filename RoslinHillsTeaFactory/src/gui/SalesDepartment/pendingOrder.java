   private void loadPendingOrder(){
       try {
           ResultSet rs = MySQL.executeSearch("SELECT * FROM `pending_order` INNER JOIN `pending_status`"
                   + "ON `pending_order`.`pending_status_id` = `pending_status`.`id`");
           
           DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           model.setRowCount(0);
           
           while(rs.next()){
           Vector vector = new Vector();
           vector.add(rs.getInt("pending_order.id"));
           vector.add(rs.getString("pending_order.customer_nic"));
           vector.add(rs.getString("order_id"));
           vector.add(rs.getString("pending_status.status"));
           
           model.addRow(vector);
           }
       } catch (Exception e) {
           e.printStackTrace();
       }
   }
   
   private void loadOrder_Status(){
       try {
           ResultSet rs = MySQL.executeSearch("SELECT * FROM `order_status`");
           Vector vector = new Vector();
           vector.add("SELECT");
           
           while (rs.next()) {               
               vector.add(rs.getString("status"));
           }
           DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
           jComboBox1.setModel(model);
       } catch (Exception e) {
       e.printStackTrace();
       }
   }
   
