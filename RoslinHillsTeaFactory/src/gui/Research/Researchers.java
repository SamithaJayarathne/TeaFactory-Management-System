    private void loadresearchers() {

        try {

            ResultSet rs = MySQL.executeSearch("SELECT * FROM `employees` WHERE `employee_type_id` = '3'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            // Center-align the cell content
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);

            // Apply the center renderer to all columns
            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }

            while (rs.next()) {
                Vector<String> vector = new Vector();
                vector.add(rs.getString("nic"));
                vector.add(rs.getString("fname"));
                vector.add(rs.getString("registered_date"));
                model.addRow(vector);
            }

        } catch (Exception e) {
        }

    }

