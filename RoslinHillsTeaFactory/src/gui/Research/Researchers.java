    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        int row = jTable1.getSelectedRow();
        
        if (evt.getClickCount() == 2) {
            if (rm != null) {
                rm.getNIC().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                rm.getReName().setText(String.valueOf(jTable1.getValueAt(row, 1)));
                
                String date = String.valueOf(jTable1.getValueAt(row, 2));
                SimpleDateFormat date2 = new SimpleDateFormat("yyyy-MM-dd");
                Date date3 = null;
                try {
                    date3 = date2.parse(date);
                } catch (ParseException ex) {
                    Logger.getLogger(Researchers.class.getName()).log(Level.SEVERE, null, ex);
                }
                rm.getReDate().setDate(date3);
                
                this.dispose();
            }
        } 

    }//GEN-LAST:event_jTable1MouseClicked

