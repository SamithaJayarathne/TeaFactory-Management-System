    private void loadResearchesTable() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet rs = MySQL.executeSearch("SELECT * FROM `reseachers`"
                    + "INNER JOIN `employees` ON `reseachers`.`employees_nic`=`employees`.`nic`");
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("employees_nic"),
                    rs.getString("employees.fname") + " " + rs.getString("employees.lname"),
                    rs.getString("employees.registered_date"),
                    rs.getString("email"),
                    rs.getString("specialization"),
                    rs.getInt("security_level")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

